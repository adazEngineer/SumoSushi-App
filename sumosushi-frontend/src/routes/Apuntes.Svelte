<script lang="ts">
    // ------------------------------------------
    // 游 INTRODUCCI칍N A SVELTE 5 CON TYPESCRIPT
    // ------------------------------------------
    // Svelte 5 usa "runas" para una reactividad clara y tipada.
    // TypeScript ayuda a detectar errores en tiempo de compilaci칩n.
  
    import { $state, $effect, $computed, $props, $derived } from 'svelte';
  
    // ------------------------------------------
    // 游 $state: Declaraci칩n de estado reactivo tipado
    // ------------------------------------------
    const count = $state<number>(0);
  
    type User = {
      name: string;
      age: number;
    };
  
    const user = $state<User>({ name: 'Alice', age: 25 });
  
    // ------------------------------------------
    // 游대 $effect: Efectos secundarios reactivos
    // ------------------------------------------
    $effect(() => {
      console.log('Count ha cambiado:', count);
    });
  
    // ------------------------------------------
    // 游빑 $computed: Derivar valores reactivamente
    // ------------------------------------------
    const double = $computed<number>(() => count * 2);
  
    // ------------------------------------------
    // 游닍 $props: Acceder a props de forma reactiva
    // ------------------------------------------
    const { title } = $props<{ title: string }>();
  
    // ------------------------------------------
    // 游댃 $derived: Combinar m칰ltiples estados
    // ------------------------------------------
    const summary = $derived<string, [number, User]>(
      [count, user],
      ([c, u]) => `Hola ${u.name}, el contador est치 en ${c}`
    );
  
    // ------------------------------------------
    // 游닋 FUNCIONES DE MANEJO TIPADAS
    // ------------------------------------------
    function increment(): void {
      count++;
    }
  
    function changeName(newName: string): void {
      user.name = newName;
    }
  </script>
  
  <!-- ------------------------------------------
  游늯 INTERFAZ DE USUARIO
  ------------------------------------------ -->
  <h1>{title}</h1>
  <p>Contador: {count}</p>
  <p>Doble: {double}</p>
  <p>{summary}</p>
  
  <button on:click={increment}>Incrementar</button>
  <button on:click={() => changeName('Bob')}>Cambiar nombre</button>
  
